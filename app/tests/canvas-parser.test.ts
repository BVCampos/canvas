import { beforeAll, describe, expect, it } from "vitest";
import { readFileSync, existsSync } from "node:fs";
import { join } from "node:path";
import {
  ASSET_UPLOAD_MIMES,
  isAllowlistedFontUrl,
  parseDeckHtml,
} from "../src/lib/canvas/parser";

const fixturesDir = join(__dirname, "fixtures");

function readFixture(name: string): string {
  return readFileSync(join(fixturesDir, name), "utf-8");
}

describe("parseDeckHtml — mini fixture", () => {
  const html = readFixture("mini-deck.html");
  const deck = parseDeckHtml(html);

  it("extracts the deck title and language", () => {
    expect(deck.title).toBe("Mini deck — fixture");
    expect(deck.lang).toBe("pt-BR");
  });

  it("captures viewport meta", () => {
    expect(deck.meta.viewport).toBe("width=device-width, initial-scale=1.0");
  });

  it("hoists every <style> block into theme_css", () => {
    expect(deck.theme_css).toContain(".slide { padding: 24px; }");
  });

  it("hoists every <script> block into nav_js", () => {
    expect(deck.nav_js).toContain('console.log("nav loaded")');
  });

  it("decomposes top-level <section class='slide'> into ordered slides", () => {
    expect(deck.slides).toHaveLength(3);
    expect(deck.slides.map((s) => s.position)).toEqual([0, 1, 2]);
  });

  it("derives slide titles from headings, eyebrow, or class modifiers", () => {
    expect(deck.slides[0].title).toBe("Cover");
    expect(deck.slides[1].title).toBe("Painéis");
    // Slide 3 has no heading; the eyebrow / class modifier picks up the slack.
    expect(deck.slides[2].title.length).toBeGreaterThan(0);
  });

  it("preserves class modifiers other than 'slide'", () => {
    expect(deck.slides[0].class_modifiers).toEqual(["cover-slide"]);
    expect(deck.slides[2].class_modifiers).toEqual(["blue-slide", "divider"]);
  });

  it("hoists base64 <img> data URLs into assets and rewrites src to placeholders", () => {
    expect(deck.assets).toHaveLength(1);
    expect(deck.assets[0].mime_type).toBe("image/png");
    expect(deck.assets[0].data.byteLength).toBeGreaterThan(0);
    expect(deck.slides[1].html_body).toContain(deck.assets[0].placeholder_id);
    expect(deck.slides[1].html_body).not.toContain("data:image/png;base64");
  });

  it("wraps slide html_body in a <section> with the original classes", () => {
    expect(deck.slides[0].html_body.startsWith('<section class="slide cover-slide">')).toBe(true);
    expect(deck.slides[0].html_body.endsWith("</section>")).toBe(true);
  });

  it("decodes HTML entities in extracted slide titles", () => {
    // We support the SGML standard names + numeric (&#nnn;, &#xhh;). Less
    // common named entities like &atilde; fall through unchanged — that's
    // documented in decodeEntities. Decks generated by Claude tend to use
    // either UTF-8 directly or the standard escape set.
    const sample = `<!DOCTYPE html><html><head><title>x</title></head><body>
      <section class="slide"><h1>Hub de FP&amp;A &amp; Comercial</h1></section>
      <section class="slide"><h2>Onde est&#227;o as &quot;dores&quot;?</h2></section>
      <section class="slide"><h1>&#233; aqui</h1></section>
    </body></html>`;
    const parsed = parseDeckHtml(sample);
    expect(parsed.slides[0].title).toBe("Hub de FP&A & Comercial");
    expect(parsed.slides[1].title).toBe('Onde estão as "dores"?');
    expect(parsed.slides[2].title).toBe("é aqui");
  });

  it("decodes HTML entities in the deck <title>, not only slide titles", () => {
    // Regression: the deck title was captured raw while slide titles were
    // decoded, so assembleDeckHtml's escapeHtml double-escaped it on export
    // (<title>Tom &amp; Jerry</title> rendered as the literal "Tom &amp; Jerry").
    const sample = `<!DOCTYPE html><html><head><title>Tom &amp; Jerry &quot;Q3&quot;</title></head>
      <body><section class="slide"><h1>x</h1></section></body></html>`;
    expect(parseDeckHtml(sample).title).toBe('Tom & Jerry "Q3"');
  });

  it("falls back to 'Untitled deck' when there is no <title>", () => {
    const sample = `<!DOCTYPE html><html><head></head>
      <body><section class="slide"><h1>x</h1></section></body></html>`;
    expect(parseDeckHtml(sample).title).toBe("Untitled deck");
  });
});

describe("parseDeckHtml — tolerant slide detection", () => {
  it("tier 2: decomposes <div class='slide'> inside a <div class='slides'> track", () => {
    // The shape a lot of Claude decks ship (a real client deck shipped exactly this) —
    // div-wrapped slides in a horizontal `.slides` strip. Used to parse to 0.
    const html = `<!DOCTYPE html><html lang="pt-BR"><head><title>Div deck</title>
      <style>.slide { padding: 24px; }</style></head><body>
      <div class="deck">
        <div class="top-nav"><div class="top-nav-section">Intro</div></div>
        <div class="slides" id="slides">
          <div class="slide"><div class="eyebrow">Diagnóstico</div><h1>Slide One</h1></div>
          <div class="slide"><h1>Slide Two</h1><div class="card">nested</div></div>
          <div class="slide cover"><h2>Slide Three</h2></div>
        </div>
      </div>
      <script>console.log("nav")</script></body></html>`;
    const deck = parseDeckHtml(html);
    expect(deck.slides).toHaveLength(3);
    expect(deck.slides.map((s) => s.title)).toEqual(["Slide One", "Slide Two", "Slide Three"]);
    // Every slide is normalised to a <section class="slide …"> wrapper.
    expect(deck.slides[0].html_body.startsWith('<section class="slide">')).toBe(true);
    expect(deck.slides[2].html_body.startsWith('<section class="slide cover">')).toBe(true);
    expect(deck.slides[2].class_modifiers).toEqual(["cover"]);
    // The `.slides` container and `.top-nav` chrome are NOT slides.
    expect(deck.theme_css).toContain(".slide { padding: 24px; }");
    expect(deck.nav_js).toContain('console.log("nav")');
  });

  it("tier 2: a nested class='slide' badge doesn't split its parent slide", () => {
    const html = `<html><head><title>x</title></head><body>
      <div class="slides">
        <div class="slide"><h1>Only One</h1><span class="slide-pill">label</span>
          <div class="slide-note">inner note with the word slide</div></div>
      </div></body></html>`;
    const deck = parseDeckHtml(html);
    expect(deck.slides).toHaveLength(1);
    expect(deck.slides[0].title).toBe("Only One");
  });

  it("tier 2: also accepts <article class='slide'>", () => {
    const html = `<html><head><title>x</title></head><body>
      <article class="slide"><h1>A</h1></article>
      <article class="slide"><h1>B</h1></article></body></html>`;
    const deck = parseDeckHtml(html);
    expect(deck.slides.map((s) => s.title)).toEqual(["A", "B"]);
    expect(deck.slides[0].html_body.startsWith('<section class="slide">')).toBe(true);
  });

  it("tier 1 still wins when canonical <section class='slide'> exist", () => {
    // A deck with real sections must NOT also pick up a decorative .slide div.
    const html = `<html><head><title>x</title></head><body>
      <section class="slide"><h1>Real One</h1><div class="slide-thumb">deco</div></section>
      <section class="slide"><h1>Real Two</h1></section></body></html>`;
    const deck = parseDeckHtml(html);
    expect(deck.slides.map((s) => s.title)).toEqual(["Real One", "Real Two"]);
  });

  it("tier 3: falls back to direct children of a #slides container when slides aren't class-marked", () => {
    const html = `<html><head><title>x</title></head><body>
      <div id="slides">
        <div class="panel"><h1>Page One</h1></div>
        <div class="panel"><h1>Page Two</h1></div>
      </div></body></html>`;
    const deck = parseDeckHtml(html);
    expect(deck.slides.map((s) => s.title)).toEqual(["Page One", "Page Two"]);
    // Original class is preserved (its own CSS targets it); normalised to <section>.
    expect(deck.slides[0].html_body.startsWith('<section class="panel">')).toBe(true);
  });

  it("returns zero slides for HTML with no recognisable slide structure", () => {
    // The importer turns this into a loud `no_slides` error rather than a deck.
    const html = `<html><head><title>x</title></head><body>
      <main><h1>Just a page</h1><p>No slides here.</p></main></body></html>`;
    const deck = parseDeckHtml(html);
    expect(deck.slides).toHaveLength(0);
  });
});

