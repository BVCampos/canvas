// CSS-background data URLs must be hoisted into assets the same way <img>
// data URLs are — otherwise a cover photo baked into theme_css as
// `background-image:url(data:…)` (the fixed-px weekly-deck case) stays inline and is
// re-sent on every slide render. The importer then rewrites the placeholder.
describe("parseDeckHtml — CSS background data URLs", () => {
  // 1×1 transparent PNG.
  const PNG =
    "data:image/png;base64,iVBORw0KGgoAAAANSUhEUgAAAAEAAAABCAQAAAC1HAwCAAAAC0lEQVR42mNk+M9QDwADhgGAWjR9awAAAABJRU5ErkJggg==";
  const html = `<!DOCTYPE html><html lang="en"><head><title>BG deck</title>
    <style>.cover-bg{position:absolute;inset:0;background-image:url('${PNG}');}</style>
    </head><body><section class="slide cover"><div class="cover-bg"></div><h1>Cover</h1></section></body></html>`;
  const deck = parseDeckHtml(html);

  it("hoists the CSS background image into assets", () => {
    expect(deck.assets).toHaveLength(1);
    expect(deck.assets[0].mime_type).toBe("image/png");
    expect(deck.assets[0].data.byteLength).toBeGreaterThan(0);
  });

  it("rewrites the data URL in theme_css to a placeholder", () => {
    expect(deck.theme_css).not.toContain("data:image/png;base64");
    expect(deck.theme_css).toContain(deck.assets[0].placeholder_id);
    // The url() wrapper survives so the CSS stays valid.
    expect(deck.theme_css).toContain(`url("${deck.assets[0].placeholder_id}")`);
  });

  it("leaves a non-extractable CSS data URL (svg → active type) inline", () => {
    const svg = "data:image/svg+xml;base64,PHN2Zy8+";
    const h = `<html><head><title>t</title><style>.x{background:url(${svg})}</style></head><body><section class="slide"><h1>x</h1></section></body></html>`;
    const d = parseDeckHtml(h);
    expect(d.assets).toHaveLength(0);
    expect(d.theme_css).toContain(svg);
  });
});

// Embedded fonts ride the same extraction path as images. Regression for the
// 2026-07-11 prod import failures: a deck whose @font-face embeds a TTF data
// URL parsed fine, then the storage upload rejected `font/ttf` and the whole
// import died. The parser must lift the font AND emit the canonical mime
// spelling the `decks` bucket allows (migration 0077).
describe("parseDeckHtml — embedded font data URLs + mime canonicalization", () => {
  const TTF = `data:font/ttf;base64,${Buffer.from("fake-ttf-bytes").toString("base64")}`;
  const html = `<!DOCTYPE html><html lang="en"><head><title>Font deck</title>
    <style>@font-face{font-family:X;src:url('${TTF}') format('truetype');}</style>
    </head><body><section class="slide"><h1>Cover</h1></section></body></html>`;
  const deck = parseDeckHtml(html);

  it("hoists a TTF @font-face data URL into assets", () => {
    expect(deck.assets).toHaveLength(1);
    expect(deck.assets[0].mime_type).toBe("font/ttf");
    expect(deck.theme_css).not.toContain("data:font/ttf");
    expect(deck.theme_css).toContain(deck.assets[0].placeholder_id);
  });

  it("normalizes alias mimes onto the canonical spelling", () => {
    const woff = `data:application/font-woff;base64,${Buffer.from("fake-woff").toString("base64")}`;
    const jpg = `data:image/jpg;base64,${Buffer.from("fake-jpg").toString("base64")}`;
    const h = `<html><head><title>t</title><style>@font-face{src:url(${woff})}</style></head><body><section class="slide"><img src="${jpg}"><h1>x</h1></section></body></html>`;
    const d = parseDeckHtml(h);
    expect(d.assets.map((a) => a.mime_type).sort()).toEqual([
      "font/woff",
      "image/jpeg",
    ]);
  });

  it("ASSET_UPLOAD_MIMES carries canonical spellings only", () => {
    for (const alias of [
      "image/jpg",
      "image/vnd.microsoft.icon",
      "application/font-woff",
      "application/font-woff2",
    ]) {
      expect(ASSET_UPLOAD_MIMES).not.toContain(alias);
    }
    expect(ASSET_UPLOAD_MIMES).toContain("font/ttf");
    expect(ASSET_UPLOAD_MIMES).toContain("font/otf");
  });
});

describe("parseDeckHtml — non-slide body chrome (chrome_html)", () => {
  // Standalone/PPTX-style deck: slides live inside .stage-area > .viewport >
  // .track wrappers; the modal overlay + dots rail are body-level siblings the
  // nav_js drives via getElementById. Dropping them (the old behaviour) made
  // onclick="openModal(...)" slides dead in Canvas.
  const html = `<!DOCTYPE html>
<html lang="pt-BR">
<head><title>Interactive deck</title>
<style>.slide{width:1280px;height:720px}</style>
</head>
<body>
  <div class="stage-area">
    <div class="viewport" id="vp">
      <div class="track" id="track">
        <section class="slide"><h1>One</h1><div class="dor" onclick="openModal('d1')">Dor 1</div></section>
        <section class="slide"><h1>Two</h1></section>
      </div>
    </div>
    <div class="arrows"><button onclick="go(-1)">←</button><button onclick="go(1)">→</button></div>
  </div>
  <div class="dots" id="dots"></div>
  <div class="overlay" id="ov"><div class="modal" id="modalBox"></div></div>
  <script>function openModal(id){ document.getElementById('modalBox').innerHTML = id; document.getElementById('ov').classList.add('open'); }</script>
</body>
</html>`;
  const deck = parseDeckHtml(html);

  it("still extracts the slides", () => {
    expect(deck.slides).toHaveLength(2);
    expect(deck.slides[0].html_body).toContain("openModal('d1')");
  });

  it("preserves body-level chrome siblings (overlay, dots)", () => {
    expect(deck.chrome_html).toContain('id="ov"');
    expect(deck.chrome_html).toContain('id="modalBox"');
    expect(deck.chrome_html).toContain('id="dots"');
  });

  it("rescues chrome nested inside slide-bearing wrappers (arrows in .stage-area)", () => {
    expect(deck.chrome_html).toContain('class="arrows"');
  });

  it("drops the slide-bearing wrappers themselves and the slides", () => {
    expect(deck.chrome_html).not.toContain('id="vp"');
    expect(deck.chrome_html).not.toContain('id="track"');
    expect(deck.chrome_html).not.toContain("<section");
    expect(deck.chrome_html).not.toContain("Dor 1");
  });

  it("strips script/style blocks from kept chrome (already hoisted)", () => {
    expect(deck.chrome_html).not.toContain("<script");
  });

  it("is empty for a canonical Canvas-native deck", () => {
    const native = readFixture("mini-deck.html");
    const nativeDeck = parseDeckHtml(native);
    // The mini fixture may keep non-slide siblings; the invariant that matters:
    // no slide markup ever leaks into chrome_html.
    expect(nativeDeck.chrome_html).not.toContain("<section");
  });
});

// The parser used to discard the whole <head>, so a deck that pulled its font
// from a `<link rel="stylesheet" href="https://fonts.googleapis.com/...">` lost
// it the instant it entered Canvas and rendered in a fallback font. We now
// preserve allowlisted font links into meta.font_links; assemble re-emits them.
describe("parseDeckHtml — preserved web-font links (meta.font_links)", () => {
  it("captures a Google Fonts <link rel='stylesheet'> href into meta.font_links", () => {
    const href = "https://fonts.googleapis.com/css2?family=Inter:wght@400;700&display=swap";
    const html = `<!DOCTYPE html><html lang="en"><head><title>Fonted deck</title>
      <link rel="preconnect" href="https://fonts.gstatic.com" crossorigin>
      <link rel="stylesheet" href="${href}">
      <style>body{font-family:'Inter',sans-serif}</style></head>
      <body><section class="slide"><h1>Hi</h1></section></body></html>`;
    const deck = parseDeckHtml(html);
    expect(deck.meta.font_links).toEqual([href]);
  });

  it("captures fonts.bunny.net and fonts.gstatic.com stylesheet links", () => {
    const bunny = "https://fonts.bunny.net/css?family=inter:400,700";
    const gstatic = "https://fonts.gstatic.com/custom/fonts.css";
    const html = `<html><head><title>x</title>
      <link rel="stylesheet" href="${bunny}">
      <link rel="stylesheet" href="${gstatic}"></head>
      <body><section class="slide"><h1>x</h1></section></body></html>`;
    const deck = parseDeckHtml(html);
    expect(deck.meta.font_links).toEqual([bunny, gstatic]);
  });

  it("does NOT capture a non-font <link> (CDN stylesheet) or non-stylesheet rels", () => {
    const html = `<html><head><title>x</title>
      <link rel="stylesheet" href="https://cdn.example.com/app.css">
      <link rel="preconnect" href="https://fonts.googleapis.com">
      <link rel="icon" href="https://fonts.gstatic.com/favicon.ico"></head>
      <body><section class="slide"><h1>x</h1></section></body></html>`;
    const deck = parseDeckHtml(html);
    // Off-allowlist host is dropped; allowlisted-but-not-stylesheet rels too.
    expect(deck.meta.font_links).toBeUndefined();
  });

  it("dedupes a repeated font link and tolerates single quotes", () => {
    const href = "https://fonts.googleapis.com/css2?family=Roboto";
    const html = `<html><head><title>x</title>
      <link rel='stylesheet' href='${href}'>
      <link rel="stylesheet" href="${href}"></head>
      <body><section class="slide"><h1>x</h1></section></body></html>`;
    const deck = parseDeckHtml(html);
    expect(deck.meta.font_links).toEqual([href]);
  });

  it("omits meta.font_links entirely when the deck has no font links", () => {
    const html = `<html><head><title>x</title></head>
      <body><section class="slide"><h1>x</h1></section></body></html>`;
    expect(parseDeckHtml(html).meta.font_links).toBeUndefined();
  });
});

describe("isAllowlistedFontUrl", () => {
  it("accepts the known font hosts and their subdomains", () => {
    expect(isAllowlistedFontUrl("https://fonts.googleapis.com/css2?family=Inter")).toBe(true);
    expect(isAllowlistedFontUrl("https://fonts.gstatic.com/s/inter/x.woff2")).toBe(true);
    expect(isAllowlistedFontUrl("https://fonts.bunny.net/css?family=inter")).toBe(true);
    // Subdomain of an allowlisted host.
    expect(isAllowlistedFontUrl("https://cdn.fonts.gstatic.com/x.woff2")).toBe(true);
  });

  it("rejects off-allowlist hosts, lookalikes, and malformed URLs", () => {
    expect(isAllowlistedFontUrl("https://evil.com/x.woff2")).toBe(false);
    // A suffix attack: notfonts.googleapis.com must NOT match googleapis.com.
    expect(isAllowlistedFontUrl("https://fonts.googleapis.com.evil.com/x")).toBe(false);
    expect(isAllowlistedFontUrl("not a url")).toBe(false);
    expect(isAllowlistedFontUrl("/relative/path.css")).toBe(false);
  });
});
